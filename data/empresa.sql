----------------------------FUNÇÃO SE EMPRESA EXISTE-------------------------------------

CREATE OR REPLACE FUNCTION EMPRESA_EXISTE(NE VARCHAR(15))
RETURNS VOID
AS $$
BEGIN
	IF NOT EXISTS(SELECT * FROM EMPRESA WHERE LOWER(nome) = LOWER(NE)) THEN
		RAISE EXCEPTION 'ESSA EMPRESA JÁ EXISTE!';
END IF;
END;
$$ LANGUAGE plpgsql;

